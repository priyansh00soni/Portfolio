const FACE_POINT_COUNT = FACE_DATA.length / 3;

/**
 * Returns { points, count, weights } scaled and positioned for the current
 * viewport, in the same shape createField().setLogoTargets() expects
 * (points is a flat Float32Array of x,y pairs; weights is a parallel
 * Float32Array of per-point ink-density weights).
 *
 * @param {number} cx   center x in screen px
 * @param {number} cy   center y in screen px
 * @param {number} size desired on-screen size (px) for the face's longest dimension
 */
export function sampleFace(cx, cy, size) {
  const count = FACE_POINT_COUNT;
  const points = new Float32Array(count * 2);
  const weights = new Float32Array(count);

  for (let i = 0; i < count; i++) {
    const nx = FACE_DATA[i * 3];
    const ny = FACE_DATA[i * 3 + 1];
    const w  = FACE_DATA[i * 3 + 2];
    points[i * 2] = cx + nx * size;
    points[i * 2 + 1] = cy + ny * size;
    weights[i] = w;
  }

  return { points, count, weights };
}